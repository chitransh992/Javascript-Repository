const month = "march"

switch(month){
    case 1:
        console.log("January")
        break;
    case 2:
        console.log("February")
        break;
    case "march":
        console.log("March")
        break;
    case 3:
        console.log("April")
        break;
    case 4:
        console.log("May")
        break;
    default:
        console.log("Not Valid")
        break;
}